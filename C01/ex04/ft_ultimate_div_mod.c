#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	div;
	int	modu;

	if (*b != 0)
	{
		div = (*a) / (*b);
			modu = (*a) % (*b);
			*a = div;
			*b = modu;
	}
}
